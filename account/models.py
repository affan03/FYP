class SkinAnalysis(models.Model):
    user = models.ForeignKey(User, on_delete=models.CASCADE)
    image = models.ImageField(upload_to='skin_images/', null=True, blank=True)
    skin_disease = models.CharField(max_length=100)
    acne_type = models.CharField(max_length=100, null=True, blank=True)
    acne_grade = models.CharField(max_length=50, null=True, blank=True)
    acne_severity = models.CharField(max_length=50, null=True, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return f"{self.user.username} - {self.skin_disease}"